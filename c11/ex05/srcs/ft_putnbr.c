#include <unistd.h>

void	ft_putnbr(int nb)
{
	long		l;
	char		c;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	l = nb;
	if (nb < 0)
	{
		write(1, "-", 1);
		l = -l;
	}
	if (l >= 10)
		ft_putnbr(l / 10);
	c = (l % 10) + '0';
	write(1, &c, 1);
}
